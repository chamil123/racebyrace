public class QuintupleM {

    private int chitRegistration_ID;
    private String quintuple_ItemID;
    private String quintuple_To;
    private String quintuple_For;
    private String quintuple_Amount;
    private String quintuple_Value;
    private String quintuple_Type;

    public QuintupleM() {
    }

    public QuintupleM(int chitRegistration_ID, String quintuple_ItemID, String quintuple_To, String quintuple_For, String quintuple_Amount, String quintuple_Value, String quintuple_Type) {
        this.chitRegistration_ID = chitRegistration_ID;
        this.quintuple_ItemID = quintuple_ItemID;
        this.quintuple_To = quintuple_To;
        this.quintuple_For = quintuple_For;
        this.quintuple_Amount = quintuple_Amount;
        this.quintuple_Value = quintuple_Value;
        this.quintuple_Type = quintuple_Type;
    }

    /**
     * @return the chitRegistration_ID
     */
    public int getChitRegistration_ID() {
        return chitRegistration_ID;
    }

    /**
     * @param chitRegistration_ID the chitRegistration_ID to set
     */
    public void setChitRegistration_ID(int chitRegistration_ID) {
        this.chitRegistration_ID = chitRegistration_ID;
    }

    /**
     * @return the quintuple_ItemID
     */
    public String getQuintuple_ItemID() {
        return quintuple_ItemID;
    }

    /**
     * @param quintuple_ItemID the quintuple_ItemID to set
     */
    public void setQuintuple_ItemID(String quintuple_ItemID) {
        this.quintuple_ItemID = quintuple_ItemID;
    }

    /**
     * @return the quintuple_To
     */
    public String getQuintuple_To() {
        return quintuple_To;
    }

    /**
     * @param quintuple_To the quintuple_To to set
     */
    public void setQuintuple_To(String quintuple_To) {
        this.quintuple_To = quintuple_To;
    }

    /**
     * @return the quintuple_For
     */
    public String getQuintuple_For() {
        return quintuple_For;
    }

    /**
     * @param quintuple_For the quintuple_For to set
     */
    public void setQuintuple_For(String quintuple_For) {
        this.quintuple_For = quintuple_For;
    }

    /**
     * @return the quintuple_Amount
     */
    public String getQuintuple_Amount() {
        return quintuple_Amount;
    }

    /**
     * @param quintuple_Amount the quintuple_Amount to set
     */
    public void setQuintuple_Amount(String quintuple_Amount) {
        this.quintuple_Amount = quintuple_Amount;
    }

    /**
     * @return the quintuple_Value
     */
    public String getQuintuple_Value() {
        return quintuple_Value;
    }

    /**
     * @param quintuple_Value the quintuple_Value to set
     */
    public void setQuintuple_Value(String quintuple_Value) {
        this.quintuple_Value = quintuple_Value;
    }

    /**
     * @return the quintuple_Type
     */
    public String getQuintuple_Type() {
        return quintuple_Type;
    }

    /**
     * @param quintuple_Type the quintuple_Type to set
     */
    public void setQuintuple_Type(String quintuple_Type) {
        this.quintuple_Type = quintuple_Type;
    }
    
    
}
