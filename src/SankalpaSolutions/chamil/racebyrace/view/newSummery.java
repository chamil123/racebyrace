    public void balance() {
        double totalpay = 0;
        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        for (int i = 0; i < dtm.getRowCount(); i++) {
            totalpay += Double.parseDouble(dtm.getValueAt(i, 3).toString());
        }
        jTextField173.setText("" + totalpay);
        double FC = Double.parseDouble(jTextField175.getText());
        double NCLC = Double.parseDouble(jTextField176.getText());
        double paperCash = Double.parseDouble(jTextField177.getText());
        double loan = Double.parseDouble(jTextField178.getText());
        double PD = Double.parseDouble(jTextField179.getText());

        double dueSubTot = FC + NCLC + paperCash + loan + PD;
        jTextField180.setText("" + dueSubTot);

        double totalPay = Double.parseDouble(jTextField173.getText());
        double comPay = Double.parseDouble(jTextField174.getText());
        double Exp = Double.parseDouble(jTextField14.getText());
        double Exe = Double.parseDouble(jTextField15.getText());
        double cash = Double.parseDouble(jTextField16.getText());

        double paySubTot = totalPay + comPay + Exp + Exe + cash;
        double due;
        double pay;
        jTextField17.setText("" + paySubTot);

        if (dueSubTot >= paySubTot) {
            jTextField181.setText("" + paySubTot);
            due = dueSubTot - paySubTot;
            jTextField182.setText("" + due);
            jTextField18.setText("0");
            jTextField19.setText("");
            pays = " ";
            dues = ": DUE";
            line1 = "_______________";
            line = " ";
        } else if (dueSubTot <= paySubTot) {
            jTextField18.setText("" + dueSubTot);
            pay = paySubTot - dueSubTot;
            jTextField19.setText("" + pay);
            jTextField181.setText("0");
            jTextField182.setText("");
            pays = "PAY :";
            dues = " ";
            line = "_______________";
            line1 = " ";

        }

        System.out.println("due sub : " + dueSubTot);
        System.out.println("pay sub : " + paySubTot);
    }
