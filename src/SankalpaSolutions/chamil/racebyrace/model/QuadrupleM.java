public class QuadrupleM {

    private int chitRegistration_ID;
    private String quadruple_ItemID;
    private String quadruple_To;
    private String quadruple_For;
    private String quadruple_Amount;
    private String quadruple_Value;
    private String quadruple_Type;

    public QuadrupleM() {
    }

    public QuadrupleM(int chitRegistration_ID, String quadruple_ItemID, String quadruple_To, String quadruple_For, String quadruple_Amount, String quadruple_Value, String quadruple_Type) {
        this.chitRegistration_ID = chitRegistration_ID;
        this.quadruple_ItemID = quadruple_ItemID;
        this.quadruple_To = quadruple_To;
        this.quadruple_For = quadruple_For;
        this.quadruple_Amount = quadruple_Amount;
        this.quadruple_Value = quadruple_Value;
        this.quadruple_Type = quadruple_Type;
    }

    /**
     * @return the chitRegistration_ID
     */
    public int getChitRegistration_ID() {
        return chitRegistration_ID;
    }

    /**
     * @param chitRegistration_ID the chitRegistration_ID to set
     */
    public void setChitRegistration_ID(int chitRegistration_ID) {
        this.chitRegistration_ID = chitRegistration_ID;
    }

    /**
     * @return the quadruple_ItemID
     */
    public String getQuadruple_ItemID() {
        return quadruple_ItemID;
    }

    /**
     * @param quadruple_ItemID the quadruple_ItemID to set
     */
    public void setQuadruple_ItemID(String quadruple_ItemID) {
        this.quadruple_ItemID = quadruple_ItemID;
    }

    /**
     * @return the quadruple_To
     */
    public String getQuadruple_To() {
        return quadruple_To;
    }

    /**
     * @param quadruple_To the quadruple_To to set
     */
    public void setQuadruple_To(String quadruple_To) {
        this.quadruple_To = quadruple_To;
    }

    /**
     * @return the quadruple_For
     */
    public String getQuadruple_For() {
        return quadruple_For;
    }

    /**
     * @param quadruple_For the quadruple_For to set
     */
    public void setQuadruple_For(String quadruple_For) {
        this.quadruple_For = quadruple_For;
    }

    /**
     * @return the quadruple_Amount
     */
    public String getQuadruple_Amount() {
        return quadruple_Amount;
    }

    /**
     * @param quadruple_Amount the quadruple_Amount to set
     */
    public void setQuadruple_Amount(String quadruple_Amount) {
        this.quadruple_Amount = quadruple_Amount;
    }

    /**
     * @return the quadruple_Value
     */
    public String getQuadruple_Value() {
        return quadruple_Value;
    }

    /**
     * @param quadruple_Value the quadruple_Value to set
     */
    public void setQuadruple_Value(String quadruple_Value) {
        this.quadruple_Value = quadruple_Value;
    }

    /**
     * @return the quadruple_Type
     */
    public String getQuadruple_Type() {
        return quadruple_Type;
    }

    /**
     * @param quadruple_Type the quadruple_Type to set
     */
    public void setQuadruple_Type(String quadruple_Type) {
        this.quadruple_Type = quadruple_Type;
    }
    
}
